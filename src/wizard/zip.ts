/**
 * Build and trigger a download of the wizard's output as a single ZIP.
 * Browser-only — relies on jszip + Blob + invisible <a download>.
 */
import JSZip from "jszip";
import type { WizardState } from "./state";
import {
  buildClaudeMd,
  buildSettingsJson,
  buildReviewCommand,
} from "./templates";

export async function downloadZip(s: WizardState): Promise<void> {
  const zip = new JSZip();

  zip.file("CLAUDE.md", buildClaudeMd(s));
  zip.file(".claude/settings.json", buildSettingsJson(s));
  if (s.includeReviewCommand) {
    zip.file(".claude/commands/review.md", buildReviewCommand());
  }

  // README for the user explaining what's in the zip
  zip.file(
    "README.txt",
    [
      "Claude Code starter config",
      "==========================",
      "",
      "Generated by the Claude Code Tour setup wizard.",
      "",
      "Extract this archive into your project root.",
      "",
      "Files:",
      "  CLAUDE.md                       — project instructions",
      "  .claude/settings.json           — permissions / model / hooks",
      ...(s.includeReviewCommand
        ? ["  .claude/commands/review.md      — /review slash-command"]
        : []),
      "",
      "After extracting, run `claude` in this directory.",
      "",
    ].join("\n"),
  );

  const blob = await zip.generateAsync({ type: "blob" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = "claude-starter.zip";
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  // Free the blob after a tick — Safari needs it to survive the click.
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}
